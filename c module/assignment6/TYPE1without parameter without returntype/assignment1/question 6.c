//Write a program to check whether a given character is uppercase or lowercase. 
#include <stdio.h>
 void checkcharater();
 
 void main()
 {
 	checkcharater();
 	
 }
 void checkcharater()
 {
 	{
 	char ch = 'A';
 	
 	if(ch>='A'&& ch<='Z')
 	{
 		printf("character is uppercase");
 	}
 	else
 	{
 	    printf("character is lowercase");
	}
 }
 }
 